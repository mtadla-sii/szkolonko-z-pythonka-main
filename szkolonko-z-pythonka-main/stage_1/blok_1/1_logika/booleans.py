"""
podstawy logiki - prawda i fałsz w Pythonie
"""
print(True)
print(False)

"""
Operatory logiczne
"""
print(True and True)
print(True and False)
print(True or True)
print(True or False)

print(False and True)
print(False and False)
print(False or True)
print(False or False)

"""
zaprzeczenie
"""
print(not True)
print(not False)

print(not True and True)
print(not True and False)
print(not True or True)
print(not True or False)

print(not False and True)
print(not False and False)
print(not False or True)
print(not False or False)

print(True and not True)
print(True and not False)
print(True or not True)
print(True or not False)

print(False and not True)
print(False and not False)
print(False or not True)
print(False or not False)

"""
zaprzeczenie całości wyrażenia
"""
print(not (True and True))
print(not (True and False))
print(not (True or True))
print(not (True or False))

print(not (False and True))
print(not (False and False))
print(not (False or True))
print(not (False or False))
